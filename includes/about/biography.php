<h2>Artist Biography</h2>

<p>I am a brighton based artist working mainly in my home studio.</p>
<p>Studied Graphic design and Art history at brighton college then went on to complete a degree in Fine Art at brighton university.</p>
<p>In the past I exhibited frequently in the smaller london galleries such as L.A.G and the Red Dog gallery and also would hold closed showings on occaison in south west london in a private studio as well as shared exhibitions more locally.</p>
<p>I use to mainly produce commission based work to meet specific requirements but now I try to paint more freely and reguarly</p>
